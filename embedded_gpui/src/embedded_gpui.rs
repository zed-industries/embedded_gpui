/// A typed handle to an entity homed on the other side of the boundary. The nearest
/// thing to `Entity<T>` that can cross a sandbox wall:
///
/// - **call methods** — [`Remote::send`] / [`Remote::call`], or the typed methods a
///   schema's generated `...Caller` trait adds (methods returning refs resolve
///   directly to connected `Remote`s);
/// - **react to it** — [`Remote::observe`] mirrors the home's `cx.notify`,
///   [`Remote::subscribe`] its typed `cx.emit` events;
/// - **refcounted** — clones share one guard; when the last clone of a ref-derived
///   remote drops, the home is told to let the entity go.
///
/// A remote is its registry handle plus an object id: it holds its registry weakly, so
/// it can outlive the boundary it came from (sends then resolve their receipts with
/// errors).
///
/// Reads are calls: state lives at the home, and anything you want to look at is a
/// method returning it (cache it locally with `embedded_gpui_util::Mirror` if you need
/// synchronous reads for rendering).
pub struct Remote<S: Interface> {
    objects: registry::WeakObjects,
    entity_id: u64,
    _guard: Option<Rc<dyn std::any::Any>>,
    _spec: PhantomData<fn() -> S>,
}

impl<S: Interface> Clone for Remote<S> {
    fn clone(&self) -> Self {
        Self {
            objects: self.objects.clone(),
            entity_id: self.entity_id,
            _guard: self._guard.clone(),
            _spec: PhantomData,
        }
    }
}

impl<S: Interface> Remote<S> {
    #[doc(hidden)]
    pub(crate) fn from_parts(
        objects: registry::WeakObjects,
        entity_id: u64,
        guard: Option<Rc<dyn std::any::Any>>,
    ) -> Self {
        Self {
            objects,
            entity_id,
            _guard: guard,
            _spec: PhantomData,
        }
    }

    /// Mint the serializable capability reference for this entity, for embedding in
    /// message or event payloads.
    pub fn reference(&self) -> Ref<S> {
        Ref::from_raw(self.entity_id)
    }

    fn request(&self, method: &str, payload: Vec<u8>, _cx: &mut gpui::App) -> Receipt<Vec<u8>> {
        let (sender, receipt) = Receipt::channel();
        match self.objects.upgrade() {
            // Dropping `sender` resolves the receipt with an error.
            None => log::warn!("embedded_gpui: send after the boundary was torn down"),
            Some(objects) => objects.send(self.entity_id, method, payload, Some(sender)),
        }
        receipt
    }

    /// The signal this remote's events land on, created on first use; a detached,
    /// never-firing stand-in if the boundary or projection is gone.
    fn signal(&self, cx: &mut gpui::App) -> gpui::Entity<RemoteSignal> {
        self.objects
            .upgrade()
            .and_then(|objects| objects.signal_for(self.entity_id, cx))
            .unwrap_or_else(|| cx.new(|_| RemoteSignal::new()))
    }

    /// Send a typed message to the entity's home. Await the receipt to know it was
    /// applied; drop it for fire-and-forget.
    pub fn send<M: Message<Spec = S>>(&self, message: M, cx: &mut gpui::App) -> Receipt {
        match encode(&message) {
            Ok(payload) => self.request(M::METHOD, payload, cx).acknowledged(),
            Err(error) => {
                log::error!(
                    "embedded_gpui: failed to encode {}::{}: {error:#}",
                    S::TYPE_NAME,
                    M::METHOD
                );
                Receipt::dropped()
            }
        }
    }

    /// Call a typed method on the entity's home, resolving with its return value.
    pub fn call<M: Message<Spec = S>>(
        &self,
        message: M,
        cx: &mut gpui::App,
    ) -> Receipt<M::Response> {
        match encode(&message) {
            Ok(payload) => self.request(M::METHOD, payload, cx).decoded(),
            Err(error) => {
                log::error!(
                    "embedded_gpui: failed to encode {}::{}: {error:#}",
                    S::TYPE_NAME,
                    M::METHOD
                );
                Receipt::dropped()
            }
        }
    }

    /// Call a typed method whose return value is a capability reference, resolving
    /// with a live [`Remote`] already connected to it: object allocation across the
    /// boundary, one call from handle to handle. The schema's generated caller uses
    /// this for every `Ref`-returning method.
    pub fn call_connecting<M, S2>(&self, message: M, cx: &mut gpui::App) -> Receipt<Remote<S2>>
    where
        M: Message<Spec = S, Response = Ref<S2>>,
        S2: Interface,
    {
        match encode(&message) {
            Ok(payload) => self
                .request(M::METHOD, payload, cx)
                .connected(self.objects.clone()),
            Err(error) => {
                log::error!(
                    "embedded_gpui: failed to encode {}::{}: {error:#}",
                    S::TYPE_NAME,
                    M::METHOD
                );
                Receipt::dropped()
            }
        }
    }

    /// The dynamic escape hatch: send an arbitrary method and payload.
    pub fn send_raw(&self, method: &str, payload: Vec<u8>, cx: &mut gpui::App) -> Receipt {
        self.request(method, payload, cx).acknowledged()
    }

    /// The dynamic call escape hatch: call an arbitrary method and decode the response.
    pub fn call_raw<R: DeserializeOwned + 'static>(
        &self,
        method: &str,
        payload: Vec<u8>,
        cx: &mut gpui::App,
    ) -> Receipt<R> {
        self.request(method, payload, cx).decoded()
    }

    /// Call a method by name with a pre-encoded payload, resolving with the raw response
    /// bytes. The forwarding primitive: a caretaker pipes these straight through.
    pub fn forward(&self, method: &str, payload: Vec<u8>, cx: &mut gpui::App) -> Receipt<Vec<u8>> {
        self.request(method, payload, cx)
    }

    /// React to the home entity's `cx.notify`, exactly like `cx.observe` on a local
    /// entity. The home notifies once on subscription, so observers always fire at least
    /// once after attaching.
    pub fn observe(
        &self,
        cx: &mut gpui::App,
        mut callback: impl FnMut(&mut gpui::App) + 'static,
    ) -> gpui::Subscription {
        let signal = self.signal(cx);
        cx.observe(&signal, move |_, cx| callback(cx))
    }

    /// React to a typed event the home emitted with `cx.emit`, exactly like
    /// `cx.subscribe` on a local entity.
    pub fn subscribe<E: Event<Spec = S>>(
        &self,
        cx: &mut gpui::App,
        mut callback: impl FnMut(&E, &mut gpui::App) + 'static,
    ) -> gpui::Subscription {
        let signal = self.signal(cx);
        cx.subscribe(&signal, move |_, event: &RawEvent, cx| {
            if event.name == E::EVENT {
                match decode::<E>(&event.payload) {
                    Ok(event) => callback(&event, cx),
                    Err(error) => log::error!(
                        "embedded_gpui: failed to decode {} event {:?}: {error:#}",
                        S::TYPE_NAME,
                        E::EVENT
                    ),
                }
            }
        })
    }

    /// React to every event from the home, undecoded: the forwarding primitive for
    /// events, mirroring [`Remote::forward`] for calls.
    pub fn subscribe_raw(
        &self,
        cx: &mut gpui::App,
        mut callback: impl FnMut(&str, &[u8], &mut gpui::App) + 'static,
    ) -> gpui::Subscription {
        let signal = self.signal(cx);
        cx.subscribe(&signal, move |_, event: &RawEvent, cx| {
            callback(&event.name, &event.payload, cx)
        })
    }
}

/// A serializable capability reference to a shared object of kind `S`: how objects
/// travel. An entity lives on one end; a [`Remote`] is how the other end holds it; a
/// `Ref` is how it moves through payloads (including call responses and events).
///
/// On the wire this is nothing but the entity id — random, so possession is the
/// authority: a ref can be known but never guessed. Sharing the same entity twice with
/// different method tables mints attenuated capabilities. Turn a ref back into a live
/// handle with `connect` (schema methods declared to return a `Ref` do this on arrival,
/// resolving with a connected `Remote`).
pub struct Ref<S: Interface> {
    entity_id: u64,
    _spec: PhantomData<fn() -> S>,
}

impl<S: Interface> Ref<S> {
    #[doc(hidden)]
    pub fn from_raw(entity_id: u64) -> Self {
        Self {
            entity_id,
            _spec: PhantomData,
        }
    }

    pub fn entity_id(&self) -> u64 {
        self.entity_id
    }
}

impl<S: Interface> Clone for Ref<S> {
    fn clone(&self) -> Self {
        *self
    }
}

impl<S: Interface> Copy for Ref<S> {}

impl<S: Interface> std::fmt::Debug for Ref<S> {
    fn fmt(&self, formatter: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(formatter, "Ref<{}>({})", S::TYPE_NAME, self.entity_id)
    }
}

impl<S: Interface> PartialEq for Ref<S> {
    fn eq(&self, other: &Self) -> bool {
        self.entity_id == other.entity_id
    }
}

impl<S: Interface> Eq for Ref<S> {}

impl<S: Interface> Serialize for Ref<S> {
    fn serialize<Ser: serde::Serializer>(&self, serializer: Ser) -> Result<Ser::Ok, Ser::Error> {
        serializer.serialize_u64(self.entity_id)
    }
}

impl<'de, S: Interface> serde::Deserialize<'de> for Ref<S> {
    fn deserialize<De: serde::Deserializer<'de>>(deserializer: De) -> Result<Self, De::Error> {
        Ok(Self::from_raw(u64::deserialize(deserializer)?))
    }
}
