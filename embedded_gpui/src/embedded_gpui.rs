//! GPUI running inside a Wasm component, embedded back into a native GPUI application.
//!
//! One crate serves both sides of the boundary:
//!
//! - the **object layer** (this file, always compiled): [`Remote`], [`Receipt`],
//!   [`Ref`], specs/messages/events, and the [`Shared`] home trait — the typed
//!   veneer over the dynamic wire, where everything is
//!   `(entity_id, method: string, payload: bytes)`;
//! - the **object registry** (`registry`, always compiled): one side-blind
//!   implementation of homes and projections that both ends run identically, configured
//!   only with a transport and an id namespace;
//! - the **host runtime** ([`PluginHost`] and friends; native targets only): wasmtime
//!   glue, display-list replay, and the byte transport for the registry;
//! - the **guest platform** (wasm32 targets only): a GPUI `Platform` implementation over
//!   the WIT protocol, plus [`Plugin`], [`register_plugin!`], and the guest-side
//!   transport (`share`, `share_root`, `root`, `connect`, and friends).
//!
//! See `DESIGN.md` at the repository root for the architecture.

